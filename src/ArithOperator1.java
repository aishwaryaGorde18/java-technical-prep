public class ArithOperator1
{
    public static void main(String[] args)
    {
        int a = 15, b = 5;

        // Add
        System.out.println("a + b = " + (a + b));

        // Sub
        System.out.println("a - b = " + (a - b));

        // Multi
        System.out.println("a * b = " + (a * b));

        // Division
        System.out.println("a / b = " + (a / b));

        // Modulus
        System.out.println("a % b = " + (a % b));
    }
}