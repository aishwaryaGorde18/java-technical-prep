public class LogicalOperator
{
    public static void main(String[] args)
    {
        boolean A = false, B = true;

        System.out.println("A && B: " + (A && B));
        System.out.println("A || B: " + (A || B));
        System.out.println("!A: " + (!A));
        System.out.println("!B: " + (!B));
    }
}
